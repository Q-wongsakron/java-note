public class MathOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        System.out.println("plus = " + (a+b));
        System.out.println("minus = " + (a-b));
        System.out.println("multi = " + (a*b));
        System.out.println("divi = " + (a/b));
        System.out.println("mod = " + (a%b));
    }
}
